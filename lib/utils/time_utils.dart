// lib/utils/time_utils.dart
import '../models/message.dart';

class TimeUtils {
  static String formatTime(DateTime dateTime) {
    final now = DateTime.now();
    final difference = now.difference(dateTime);
    
    if (difference.inDays > 0) {
      return '${dateTime.day}/${dateTime.month}/${dateTime.year}';
    } else if (difference.inHours > 0) {
      return 'hace ${difference.inHours}h';
    } else if (difference.inMinutes > 0) {
      return 'hace ${difference.inMinutes}m';
    } else {
      return 'ahora';
    }
  }

  static String getLastUpdateTime(List<Message> messages) {
    if (messages.isEmpty) return 'Sin mensajes';
    
    final lastMessage = messages.last;
    final now = DateTime.now();
    final difference = now.difference(lastMessage.timestamp);
    
    if (difference.inDays > 0) {
      return 'hace ${difference.inDays} dÃ­a${difference.inDays > 1 ? 's' : ''}';
    } else if (difference.inHours > 0) {
      return 'hace ${difference.inHours} hora${difference.inHours > 1 ? 's' : ''}';
    } else if (difference.inMinutes > 0) {
      return 'hace ${difference.inMinutes} minuto${difference.inMinutes > 1 ? 's' : ''}';
    } else {
      return 'ahora mismo';
    }
  }
}
